Một cách trực quan, ý tưởng độc nhất của dòng xe Porsche cỡ lớn này là sự tương phản của một thiết kế đầy biểu cảm mới: một chiếc Panamera và một chiếc xe thể thao không thể nhầm lẫn – với hình dáng dài và nhanh nhẹn, đường gân dọc thân xe sắc cạnh, hông xe thể thao và đường cắt mui xe cực dốc đến mức thấp hơn 20 mm tại đuôi xe. Đường cắt mui xe điển hình của Porsche tạo sự liên tưởng đến biểu tượng thiết kế của Porsche, huyền thoại 911.

Nhiều tính năng và tương tác dễ dàng trong Buồng lái Tiên tiến của Porsche

Thiết kế nội thất điển hình của Porsche được diễn giải theo xu hướng tương lai trong mẫu Panamera mới. Bề mặt táp lô màu đen với màn hình hiển thị cảm ứng kết hợp giao diện người dùng rõ ràng và trực quan như trên điện thoại và máy tính bảng với những điều kiện cần thiết trong việc kiểm soát chiếc xe. Các nút bấm và cụm điều khiển thông thường được giảm đáng kể. Các chi tiết này được thay thế bởi bảng điều khiển cảm ứng và những màn hình tùy biến cá nhân nằm ngay giữa Buồng lái Tiên tiến của Porsche – mang lại nhiều lợi ích cho người lái cũng như hành khách phía trước và phía sau. Dù được mở rộng một cách đáng kể, các chức năng liên lạc, tiện ích và hệ thống hỗ trợ giờ đây có thể được sử dụng và điều khiển một cách rõ ràng và trực quan hơn. Buồng lái Tiên tiến của Porsche biến thế giới của những nút bấm trở thành kỹ thuật điện tử hiện đại của tính di động, dành nhiều chỗ hơn cho sự đam mê. Đồng hồ vòng tua, được đặt ngay giữa cụm đồng hồ, là một dụng cụ gợi nhắc đến chiếc Porsche 356 A đời 1955.

Động cơ V6 và V8 tăng áp kép mới cho công suất và âm thanh mạnh mẽ

Một chiếc Porsche luôn gây ấn tượng không chỉ về sức mạnh mà hiệu suất vận hành cũng quan trọng không kém. Nhằm đẩy công thức này lên một tầm cao mới, tất cả động cơ của mẫu xe Panamera thế hệ thứ hai đã được tái thiết kế. Các động cơ này được tạo ra không chỉ với công suất lớn hơn mà còn cải thiện đáng kể mức tiêu thụ nhiên liệu và lượng khí thải. Ba loại động cơ phun nhiên liệu trực tiếp tăng áp kép được giới thiệu trong buổi ra mắt thị trường: thuộc các phiên bản Panamera Turbo, Panamera 4S và Panamera 4S Diesel. Tất cả các động cơ này – và lần đầu tiên bao gồm cả động cơ diesel – đều có thể được trang bị với hệ thống dẫn động bốn bánh toàn thời gian và hộp số ly hợp kép tám cấp mới của Porsche (PDK). Động cơ xăng V8 cung cấp 404 kW (550 mã lực) cho Panamera Turbo và động cơ xăng V6 truyền 324 kW (440 mã lực) cho Panamera 4S. Phiên bản Panamera 4S Diesel sở hữu động cơ V8 với 310 kW (422 mã lực) cho công suất mạnh mẽ và mô men xoắn cực đại đạt 850 Nm.

Một mẫu saloon sang trọng trên đường đua

Bắt nhịp với ý tưởng tổng thể của mẫu Panamera hoàn toàn mới, hệ thống khung gầm cũng mang lại sự thoải mái trên hành trình dài của một mẫu saloon sang trọng với hiệu suất của một chiếc xe thể thao thực thụ. Để đạt được điều này, khung gầm vốn ấn tượng nay được trang bị thêm những hệ thống mang tính đột phá như hệ thống treo khí nén với công nghệ ba buồng khí, bao gồm Hệ thống Treo Chủ động của Porsche (PASM kiểm soát giảm sóc điện tử), Hệ thống Kiểm soát Khung gầm Chủ động Thể thao của Porsche được tăng cường (PDCC Sport) bao gồm Hệ thống Điều phối Lực kéo Cao cấp của Porsche (PTV Plus) và hệ thống cân bằng chủ động cũng như hệ thống đánh lái cơ chế điện tử mới. Hệ thống Kiểm soát Khung gầm 4D tích hợp giúp phân tích và đồng bộ hóa tất cả các hệ thống treo trong thời gian thực tế và tối ưu hóa khả năng vận hành trên đường của mẫu Panamera mới. Porsche hiện cũng đưa tính năng đánh lái chuẩn xác và khả năng điều khiển của xe thể thao vào dòng xe Gran Turismo với tính năng đánh lái trục sau – đây cũng là một điểm mới và được lấy cảm hứng từ dòng xe 918 Spyder và 911 Turbo. Hiệu suất hoạt động của phanh cũng được cải thiện.

Hệ thống hỗ trợ thế hệ tiếp theo

Panamera mới được trang bị nhiều hệ thống hỗ trợ cơ bản và tùy chọn giúp việc lái xe trở nên thuận tiện và an toàn hơn. Các hệ thống mới quan trọng nhất bao gồm hệ thống hỗ trợ tầm nhìn ban đêm, sử dụng camera tầm nhiệt để phát hiện người đi đường, động vật lớn và hiển thị đèn báo với màu sắc nổi bật trong khoang lái. Nếu lựa chọn đèn pha ma trận LED mới với 84 điểm ảnh, người đi đường khuất tầm chiếu sáng của đèn cốt cũng sẽ được chiếu sáng nếu nằm trong lộ trình được tính toán sẵn, giúp người lái phản xạ nhanh hơn. Hệ thống hỗ trợ tầm nhìn ban đêm mới là một trong những hệ thống giúp phòng ngừa những tình huống nguy cấp. Tầm nhìn xa hơn nữa chính là hệ thống Porsche InnoDrive bao gồm tính năng kiểm soát hành trình chủ động. Dựa trên dữ liệu dẫn đường và tín hiệu từ cảm biến ra-đa và video, hệ thống này tính toán trước đồng thời kích hoạt tỉ lệ tăng và giảm tốc tối ưu cũng như chuyển số phù hợp và giai đoạn chạy trớn, cho 3 km tiếp theo. Theo đó, hệ thống trợ lái điện tử còn hỗ trợ tự động tại các góc cua, đoạn đường dốc và tốc độ hạn chế.

Chi tiết hơn về động cơ mới: gia tăng sức mạnh, giảm mức tiêu thụ nhiên liệu

Mới, mạnh mẽ và tiết kiệm nhiên liệu: động cơ tăng áp V6 và V8 của mẫu xe Panamera. Và tất cả các loại động cơ này đều chia sẻ chung một ý tưởng thiết kế đặc trưng, điều mà trong thuật ngữ của các nhà phát triển động cơ gọi là “những phần nóng bỏng nằm bên trong”. Nghĩa là động cơ tăng áp của mẫu xe Panamera mới được tích hợp ngay giữa chữ V tạo bởi hai hàng xy-lanh. Bộ tăng áp đặt giữa này mang lại vô số lợi ích: Động cơ nhỏ gọn hơn, cho phép vị trí đặt động cơ thấp hơn. Điều này cũng mang lại hiệu quả tích cực đối với trọng tâm xe. Ống dẫn giữa hai bộ tăng áp và buồng đốt nhiên liệu ngắn tạo nên phản hồi chân ga tức thì. Phản hồi động cơ có thể được cải thiện tốt hơn bởi tùy chọn Thay đổi Chế độ lái với Nút bấm Phản hồi Thể thao. Nút Thay đổi Chế độ lái, lần đầu tiên được giới thiệu trong chiếc Porsche 918 Spyder, là một núm vặn trên vô-lăng có thể sử dụng để kích hoạt bốn chế độ lái (Bình thường, Thể thao, Thể thao Cao cấp và Cá nhân). Nằm ngay giữa núm vặn này là Nút bấm Phản hồi Thể thao. Nút bấm này giải phóng tiềm lực công suất tối đa của chiếc Panamera chỉ với một thao tác.

Porsche sử dụng động cơ tăng áp kép tinh vi

Đầu tiên phải kể đến phiên bản Panamera Turbo với động cơ xăng mạnh mẽ nhất trong dòng xe này. Động cơ V8 4,0 lít tăng áp kép cho công suất 550 mã lực (404 kW) tại vòng tua 5.750 vòng/phút và mô-men xoắn cực đại đạt 770 Nm (trong khoảng vòng tua từ 1.960 – 4.500 vòng/phút). Động cơ này có công suất tăng thêm 30 mã lực so với mẫu trước đây, và mô-men xoắn cực đại được tăng cường thêm 70 Nm. Động cơ tám xy-lanh giúp chiếc Panamera Turbo tăng tốc đạt 100km/giờ trong 3,8 giây; với Gói Kích hoạt Chức năng Thể thao thời gian tăng tốc chỉ còn 3,6 giây. Chiếc Porsche này có thể đạt vận tốc tối đa 306 km/giờ. Đây là những chỉ số ấn tượng cho thấy khả năng tăng tốc dễ dàng của động cơ trên chiếc Panamera mới với tỉ lệ công suất trên trọng lượng chỉ 3,6 kg/mã lực. Những số liệu về hiệu suất phi thường lại đối nghịch với mức tiêu thụ nhiên liệu 9,4 – 9,3 lít/100 km, thấp hơn đến 1,1 lít/100 km so với mẫu xe trước đây (theo tiêu chuẩn NEDC). Kèm theo đó là lượng khí thải CO2 ở mức 214 – 212 g/km.

Porsche sử dụng bộ tăng áp kép để cung cấp khí nén cho buồng đốt động cơ V8. Hai bộ tăng áp nghịch chiều sản sinh mô-men xoắn cực đại tại vòng tua máy rất thấp. Panamera Turbo cũng là phiên bản Porsche đầu tiên được trang bị chức năng kiểm soát xy-lanh thích nghi trong động cơ. Khi vận hành bán phần, hệ thống tạm thời chuyển động cơ 8 xy-lanh thành động cơ 4 xy-lanh một cách êm ái. Điều này làm giảm lượng nhiên liệu tiêu thụ đến 30%, tùy theo nhu cầu công suất trong giai đoạn 4 xy-lanh.

Động cơ V6 2,9 lít tăng áp kép của phiên bản Panamera 4S cho công suất cực đại 324 kW (440 mã lực), nhiều hơn 20 mã lực so với mẫu trước đây; tại vòng tua 5.650 vòng/phút chỉ cao hơn vòng tua bình thường một chút. Giữa vòng tua 1.750 và 5.500 vòng/phút, động cơ sáu xy-lanh cung cấp 550 Nm (nhiều hơn 30 Nm) đến các trục xe. Panamera 4S có thể đạt vận tốc 100 km/giờ chỉ trong 4,4 giây (4,2 giây với Gói Kích hoạt Chức năng Thể thao). Chiếc Porsche này gần đạt đến mốc 300 km/giờ với tốc độ tối đa 289 km/giờ. Mức tiêu thụ nhiên liệu kết hợp theo tiêu chuẩn NEDC: 8,2 – 8,1 lít/100 km (lượng khí thải CO2: 186 – 184 g/km). So sánh với Panamera thế hệ đầu tiên cho thấy lượng nhiên liệu tiết kiệm được là 1,0 lít/ 100 km hay 11%.

Tương tự động cơ tám xy-lanh trên Panamera Turbo, động cơ sáu xy-lanh của Panamera 4S cũng được trang bị hệ thống phun xăng trực tiếp đặt trong buồng đốt. Vị trí vòi phun này cung cấp khả năng đốt tối ưu, hiệu quả tối đa và động cơ phản hồi tốt. Các phiên bản 4S và Turbo cũng được cá tính hóa bởi âm thanh mạnh mẽ và đậm chất Porsche.

Panamera mới trang bị khối động cơ diesel tám xy-lanh hoàn toàn mới, lần đầu tiên được kết hợp với hệ thống dẫn động bốn bánh toàn thời gian. Đây là khối động cơ diesel mạnh nhất được trang bị trên một chiếc Porsche, cung cấp 310 kW (422 mã lực) tại vòng tua 3.500 vòng/phút và mô-men xoắn cực đại đạt 850 Nm với vòng tua động cơ liên tục trong khoảng từ 1.000 đến 3.250 vòng/phút. Với vận tốc tối đa 285 km/giờ, Porsche Panamera 4S Diesel hiện là mẫu xe thương mại nắm giữ khối động cơ diesel nhanh nhất thế giới. Dòng xe Gran Turismo tăng tốc đến 100 km/giờ chỉ trong trong 4,5 giây (4,3 giây với Gói Kích hoạt Chức năng Thể thao). Trong khi đó, mức tiêu thụ nhiên liệu kết hợp chỉ 6,8 – 6,7 lít/100 km (lượng khí thải CO2: 178 – 176 g/km).

Phiên bản diesel cũng được trang bị động cơ tăng áp kép đặt giữa. Khác với khối động cơ phun nhiên liệu trực tiếp Common Rail vốn có (áp suất phun tối đa 2.500 bar) được trang bị bộ tăng áp nối tiếp. Điều này cho phép khối động cơ hoạt động như một bộ tăng áp kép hoặc một bộ tăng áp đơn, tùy thuộc vào điều kiện hoạt động. Ở tốc độ thấp và tốc độ vừa phải, toàn bộ dòng khí thải được điều chỉnh chỉ thông qua một trong hai bộ tăng áp, giúp cải thiện phản hồi chân ga. Khối động cơ tăng áp thứ hai không hoạt động cho đến khi tốc độ động cơ đạt đến vòng tua 2.700 vòng/phút hoặc cao hơn. Cả hai khối tăng áp đều có cấu trúc tua-bin biến thiên hình học (VTG) – công nghệ thừa hưởng từ 911 Turbo.

Các chi tiết thiết kế mới: tăng tính năng động

